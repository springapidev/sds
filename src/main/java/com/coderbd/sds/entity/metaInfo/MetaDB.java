package com.coderbd.sds.entity.metaInfo;

import javax.persistence.*;
import java.io.Serializable;

/**
 *
 * @author Rajaul Islam
 */

@Entity
@Table(name = "meta_info")
public class MetaDB implements Serializable {

    private static final long serialVersionUID = 1L;
    @Id
    @GeneratedValue(strategy = GenerationType.SEQUENCE)
    @Basic(optional = false)
    @Column(name = "ID")
    private Integer id;
    @Column(name = "dbName")
    private String dbName;
    @Basic(optional = false)
    @Column(name = "start_time")
    private int startTime;
    @Basic(optional = false)
    @Column(name = "end_time")
    private int endTime;

    public MetaDB() {
    }

    public MetaDB(Integer id) {
        this.id = id;
    }

    public MetaDB(Integer id, int startTime, int endTime) {
        this.id = id;
        this.startTime = startTime;
        this.endTime = endTime;
    }

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public String getDbName() {
        return dbName;
    }

    public void setDbName(String dbName) {
        this.dbName = dbName;
    }

    public int getStartTime() {
        return startTime;
    }

    public void setStartTime(int startTime) {
        this.startTime = startTime;
    }

    public int getEndTime() {
        return endTime;
    }

    public void setEndTime(int endTime) {
        this.endTime = endTime;
    }

    @Override
    public int hashCode() {
        int hash = 0;
        hash += (id != null ? id.hashCode() : 0);
        return hash;
    }

    @Override
    public boolean equals(Object object) {
        // TODO: Warning - this method won't work in the case the id fields are not set
        if (!(object instanceof MetaDB)) {
            return false;
        }
        MetaDB other = (MetaDB) object;
        if ((this.id == null && other.id != null) || (this.id != null && !this.id.equals(other.id))) {
            return false;
        }
        return true;
    }

    
}
